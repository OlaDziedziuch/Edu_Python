# Write a function which converts the input string to uppercase.

def make_uppercase(txt):
    return txt.upper()

print(make_uppercase("hello"))                       # ➞ HELLO
print(make_uppercase("hello world"))                 # ➞ HELLO WORLD
print(make_uppercase("heLlO wORLd !"))               # ➞ HELLO WORLD !
print(make_uppercase("1,2,3 hello world!"))          # ➞ 1,2,3 HELLO WORLD!
